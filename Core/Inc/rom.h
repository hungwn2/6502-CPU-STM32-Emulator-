#ifndef ROM_H
#define ROM_H

#include <stdint.h>

//PC will hold this, a program that writes "HELLO" over uart at port 0xF001, and toggles LED at
// 0x8000 in a loop
const uint8_t rom[4096]={
    [0x000]=0xA9, [0x001]=0x48, //LDA #'H'
    [0x002]=0x8D, [0x003]=0x01, [0x004]=0xF0,
    [0x005]=0xA9, [0x006]=0x45, //LDA #'E'
    [0x007]=0x8D, [0x008]=0x01, [0x009]=0xF0,
    [0x00A]=0xA9, [0x00B]=0x4C,  //LDA #'L'
    [0x00C]=0x8D, [0x00D]=0x01, [0x011]=0xF0,
    [0x00F] = 0xA9, [0x010] = 0x4C,       // LDA #'L'
    [0x011] = 0x8D, [0x012] = 0x01, [0x013] = 0xF0, // STA $F001
    [0x014] = 0xA9, [0x015] = 0x4F,       // LDA #'O'
    [0x014]=0xA9, [0x015]=0x4F,  [0x016]=0xF0,
    [0x014] = 0xA9, [0x015] = 0x4F,       // LDA #'O'
    [0x019] = 0xA9, [0x01A] = 0x0A,       // LDA #$0A
    [0x01B] = 0x8D, [0x01C] = 0x01, [0x01D] = 0xF0, // STA $F001

    //LED loop

	[0x01E] = 0xA9, [0x01F] = 0x01,       // LDA #$01
	[0x020] = 0x8D, [0x021] = 0x00, [0x022] = 0x80, // STA $8000
	[0x023] = 0xEA,                        // NOP
	[0x024] = 0xEA,                        // NOP
	[0x025] = 0xA9, [0x026] = 0x00,       // LDA #$00
	[0x027] = 0x8D, [0x028] = 0x00, [0x029] = 0x80, // STA $8000
	[0x02A] = 0xEA,                        // NOP
	[0x02B] = 0xEA,                        // NOP
	[0x02C] = 0x4C, [0x02D] = 0x1E, [0x02E] = 0xF0, // JMP $F01E
	// Vectors at end of ROM ($FFFA-$FFFF) => offsets 0xFFA-0xFFF
	// NMI vector (unused)
	[0xFFA] = 0x00, [0xFFB] = 0xF0,       // NMI -> $F000 (ok)
	// RESET vector
	[0xFFC] = 0x00, [0xFFD] = 0xF0,       // RESET -> $F000
	// IRQ/BRK vector (unused)
	[0xFFE] = 0x00, [0xFFF] = 0xF0,       // IRQ -> $F000 (ok)
};

#endif

